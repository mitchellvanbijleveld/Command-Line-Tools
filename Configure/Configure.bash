# FIND UTILITY HELP FOLDER PATH
##################################################
FindUtilitySubFolderPath "Configure" $CONFIGURE_VAR_UTILITY
##################################################
# FIND UTILITY HELP FOLDER PATH
##################################################
##################################################
#
#
#
#
#
##################################################
##################################################
# FIND UTILITY SCRIPT HELP FILE PATH
##################################################
FindUtilityScriptSubFilePath "Configure" $CONFIGURE_VAR_UTILITY $CONFIGURE_VAR_UTILITY_SCRIPT
##################################################
# FIND UTILITY SCRIPT HELP FILE PATH
##################################################
##################################################
#
#
#
#
#
##################################################
##################################################
# START HELP UTILITY SCRIPT
##################################################
PrintMessage "DEBUG" "$VAR_UTILITY" "$VAR_UTILITY_SCRIPT" "Starting Configure Utility '$CONFIGURE_VAR_UTILITY' Script '$CONFIGURE_VAR_UTILITY_SCRIPT'..."
$(which bash) $VAR_UTILITY_SCRIPT_FILE_PATH