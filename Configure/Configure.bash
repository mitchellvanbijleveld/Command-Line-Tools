VAR_UTILITY="Configure"
VAR_UTILITY_SCRIPT="Configure"
VAR_UTILITY_SCRIPT_VERSION="2025.01.01-2318"
VAR_UTILITY_SCRIPT_REQUIRED_COMMAND_LINE_TOOLS="echo exit PrintMessage shift tr"
####################################################################################################
# UTILITY SCRIPT INFO - Configure/Configure
####################################################################################################
####################################################################################################
#
#
#
#
#
####################################################################################################
####################################################################################################
# DEFAULT VARIABLES
####################################################################################################
