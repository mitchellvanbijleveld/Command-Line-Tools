VAR_UTILITY="Hello"
VAR_UTILITY_SCRIPT="World"
VAR_UTILITY_SCRIPT_VERSION="2024.12.23-0247"
VAR_UTILITY_SCRIPT_REQUIRED_COMMAND_LINE_TOOLS="echo exit PrintMessage shift tr"
